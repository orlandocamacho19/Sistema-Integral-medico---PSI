    private void renderPatientInfo(Patient patient){
        jLabel4.setText(patient.getName());
        jLabel1.setText(patient.getPhone());
        
        roundedIndicator1.setBackground(new Color(79,195,97));
                
        List<Appointment> appointments = AppointmentControl.getInstance().getAppointmentFromNowOn();
        if (appointments != null) {
            for (Appointment appointment : appointments) {
                
                if (appointment.getPatient().getID() == patient.getID() && !appointment.isConfirmation()) {
                    roundedIndicator1.setBackground(new Color(253, 84, 83));
                    break;
                }
            }
        }
    }

