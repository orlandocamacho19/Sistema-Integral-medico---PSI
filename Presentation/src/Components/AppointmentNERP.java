    public void renderAppointmentRP(Appointment appointment){
        jLabel1.setText("Px: " + PatientControl.getInstance().getPatientsByID(appointment.getPatient()));
        
        if (appointment.getType() == Type.New) {
            jLabel2.setText("N");
            if (appointment.isConfirmation()) {
                if (appointment.getaType() == AppointmentType.Nutritional) {
                    rPBorder.setBackground(nutritionalN);
                    roundedApp1.setBackground(nutritionalN);
                } else {
                    rPBorder.setBackground(estheticN);
                    roundedApp1.setBackground(estheticN); 
                }
            } else {
                if (appointment.getaType() == AppointmentType.Nutritional) {
                    rPBorder.setBackground(nutritionalN);
                    roundedApp1.setBackground(Color.white);
                } else {
                    rPBorder.setBackground(estheticN);
                    roundedApp1.setBackground(Color.white); 
                }
            }
        } else {
            jLabel2.setText("R");
            if (appointment.isConfirmation()) {
                if (appointment.getaType() == AppointmentType.Nutritional) {
                    rPBorder.setBackground(nutritionalR);
                    roundedApp1.setBackground(nutritionalR);
                } else {
                    rPBorder.setBackground(estheticR);
                    roundedApp1.setBackground(estheticR); 
                }
            } else {
                if (appointment.getaType() == AppointmentType.Nutritional) {
                    rPBorder.setBackground(nutritionalR);
                    roundedApp1.setBackground(Color.white);
                } else {
                    rPBorder.setBackground(estheticR);
                    roundedApp1.setBackground(Color.white); 
                }
            }
        }
    }

